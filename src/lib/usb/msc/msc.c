/*
 *  MSC Get Command Block Wrapper Callback
 *    Parameters:      None (global variables)
 *    Return Value:    None
 */
void MSC_GetCBW(void)
{
  U32 n;

  for (n = 0; n < BulkLen; n++)
  {
    *((U8 *)&CBW + n) = BulkBuf[n];
  }
  if ((BulkLen == sizeof(CBW)) && (CBW.dSignature == MSC_CBW_Signature))
  {
    /* Valid CBW */
    CSW.dTag = CBW.dTag;
    CSW.dDataResidue = CBW.dDataLength;
    if ((CBW.bLUN != 0) || (CBW.bCBLength < 1) || CBW.bCBLength > 16)
    {
fail: CSW.bStatus = CSW_CMD_FAILED;
      MSC_SetCSW();
    }
    else
    {
      switch (CBW.CB[0])
      {
        case SCSI_TEST_UNIT_READY:
          MSC_TestUnitReady();
          break;
        case SCSI_REQUEST_SENSE:
          MSC_RequestSense();
          break;
        case SCSI_FORMAT_UNIT:
          goto fail;
        case SCSI_INQUIRY:
          MSC_Inquiry();
          break;
        case SCSI_START_STOP_UNIT:
          goto fail;
        case SCSI_MEDIA_REMOVAL:
          goto fail;
        case SCSI_MODE_SELECT6:
          goto fail;
        case SCSI_MODE_SENSE6:
          MSC_ModeSense6();
          break;
        case SCSI_MODE_SELECT10:
          goto fail;
        case SCSI_MODE_SENSE10:
          MSC_ModeSense10();
          break;
        case SCSI_READ_FORMAT_CAPACITIES:
          MSC_ReadFormatCapacity();
          break;
        case SCSI_READ_CAPACITY:
          MSC_ReadCapacity();
          break;
        case SCSI_READ10:
          if (MSC_RWSetup())
          {
            if ((CBW.bmFlags & 0x80) != 0)
            {
              /* Turn On Read LED */
              //GPIOB->ODR |= LED_RD;
              BulkStage = MSC_BS_DATA_IN;
              MSC_MemoryRead();
            }
            else
            {
              USB_EpSetStall(MSC_EP_OUT);
              CSW.bStatus = CSW_PHASE_ERROR;
              MSC_SetCSW();
            }
          }
          break;
        case SCSI_WRITE10:
          if (MSC_RWSetup())
          {
            if ((CBW.bmFlags & 0x80) == 0)
            {
              /* Turn On Write LED */
              //GPIOB->ODR |= LED_WR;
              BulkStage = MSC_BS_DATA_OUT;
            }
            else
            {
              USB_EpSetStall(MSC_EP_IN);
              CSW.bStatus = CSW_PHASE_ERROR;
              MSC_SetCSW();
            }
          }
          break;
        case SCSI_VERIFY10:
          if (MSC_RWSetup())
          {
            if ((CBW.bmFlags & 0x80) == 0)
            {
              BulkStage = MSC_BS_DATA_OUT;
              MemOK = TRUE;
            }
            else
            {
              USB_EpSetStall(MSC_EP_IN);
              CSW.bStatus = CSW_PHASE_ERROR;
              MSC_SetCSW();
            }
          }
          break;
        default:
          goto fail;
      }
    }
  }
  else
  {
    /* Invalid CBW */
    USB_EpSetStall(MSC_EP_IN);
    USB_EpSetStall(MSC_EP_OUT);
    BulkStage = MSC_BS_ERROR;
  }
}

